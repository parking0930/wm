package com.wmproject.domain;

import java.util.Date;

public class MemberVO {
	private String id;
	private String pw;
	private String nickname;
	private int point;
	private int win;
	private int lose;
	private Date date;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public int getWin() {
		return win;
	}
	public void setWin(int win) {
		this.win = win;
	}
	public int getLose() {
		return lose;
	}
	public void setLose(int lose) {
		this.lose = lose;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	@Override
	public String toString() {
		return "memberVO [id=" + id + ", pw=" + pw + ", nickname=" + nickname + ", point=" + point + ", win=" + win
				+ ", lose=" + lose + ", date=" + date + "]";
	}
	
}
